"""Property-based tests (Hypothesis) for dotenv loading behaviour.

**Validates: Requirements 1.3**
"""

from __future__ import annotations

import os
import tempfile
from pathlib import Path

import pytest
from hypothesis import HealthCheck, given, settings
from hypothesis import strategies as st

from llm_benchmark.cli.main import main

# ---------------------------------------------------------------------------
# Strategies
# ---------------------------------------------------------------------------

_key_strategy = st.from_regex(r"[A-Z][A-Z0-9_]{0,19}", fullmatch=True)

_value_strategy = st.text(
    alphabet=st.characters(
        whitelist_categories=("L", "N", "P", "S"),
        whitelist_characters=" ",
    ),
    min_size=1,
    max_size=40,
).filter(lambda value: "\n" not in value and "\r" not in value and "\x00" not in value)


# ---------------------------------------------------------------------------
# Property 1: Pre-existing environment variables are never overwritten by .env
# Validates: Requirements 1.3
# ---------------------------------------------------------------------------


@given(
    pairs=st.lists(
        st.tuples(_key_strategy, _value_strategy),
        min_size=1,
        max_size=5,
        unique_by=lambda pair: pair[0],
    )
)
@settings(max_examples=50, suppress_health_check=[HealthCheck.function_scoped_fixture])
def test_preexisting_env_vars_not_overwritten_by_dotenv(
    pairs: list[tuple[str, str]],
) -> None:
    """Pre-existing environment variables are never overwritten by .env values.

    For any set of KEY=value pairs already present in the environment, calling
    ``main([])`` with a ``.env`` file that contains different values for those
    same keys must leave the original values intact.

    **Validates: Requirements 1.3**

    Parameters
    ----------
    pairs : list[tuple[str, str]]
        Arbitrary KEY=value pairs generated by Hypothesis.
    """
    with tempfile.TemporaryDirectory() as tmp_dir:
        tmp_path = Path(tmp_dir)

        # Capture keys to clean up after the test
        keys = [key for key, _ in pairs]
        original_env = {key: os.environ.get(key) for key in keys}

        try:
            # Set original values in the environment
            for key, original_value in pairs:
                os.environ[key] = original_value

            # Write a .env file with different values for the same keys
            dotenv_lines = "\n".join(f"{key}=dotenv_override_{key.lower()}" for key, _ in pairs)
            (tmp_path / ".env").write_text(dotenv_lines + "\n", encoding="utf-8")

            original_cwd = Path.cwd()
            os.chdir(tmp_path)
            try:
                with pytest.raises(SystemExit):
                    main([])
            finally:
                os.chdir(original_cwd)

            # Assert all original values are preserved
            for key, original_value in pairs:
                assert os.environ[key] == original_value, (
                    f"Environment variable {key!r} was overwritten: "
                    f"expected {original_value!r}, got {os.environ[key]!r}"
                )

        finally:
            # Restore environment to its original state
            for key, previous_value in original_env.items():
                if previous_value is None:
                    os.environ.pop(key, None)
                else:
                    os.environ[key] = previous_value
